/** 兜底清洗：除括号动作描写外，再把大脑可能带出的星号加粗、破折号堆砌等程序化抹掉。 */
export function cleanVoice(text: string): string {
  return (text || '')
    .replace(/（[^）]*）/g, '')            // 去全角括号整组（中文动作/神态描写几乎都是全角）
    .replace(/\*\*([^*\n]+)\*\*/g, '$1')   // 去 Markdown 加粗 **x**
    .replace(/__([^_\n]+)__/g, '$1')       // 去 __x__
    .replace(/\*([^*\n]+)\*/g, '$1')       // 去单星斜体 *x*
    .replace(/\s*[—–]+\s*/g, '，')          // 破折号(— – ——) 一律改逗号，杜绝破折号连接
    .replace(/，{2,}/g, '，')               // 合并重复逗号
    .replace(/，(?=[。！？；、])/g, '')       // 逗号紧跟其它标点 → 去掉逗号
    .replace(/[ \t]+\n/g, '\n')
    .replace(/\n{2,}/g, '\n')
    .trim();
}
